public class Acao {
	
	int energia;
	int forca;
	int saude;
	int felicidade;
	int magia;
	int comida;
	
	public Acao() {
		energia = 0;
		forca = 0;
		saude = 0;
		felicidade = 0;
		magia = 0;
		comida = 0;
	}

	public int getEnergia() {
		return energia;
	}

	public int getForca() {
		return forca;
	}

	public int getSaude() {
		return saude;
	}

	public int getFelicidade() {
		return felicidade;
	}

	public int getMagia() {
		return magia;
	}

	public int getComida() {
		return comida;
	}

}